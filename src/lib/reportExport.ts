export function markdownToPlainText(markdown: string) {
  return markdown
    .replace(/^#{1,6}\s+/gm, "")
    .replace(/\*\*(.*?)\*\*/g, "$1")
    .replace(/\*(.*?)\*/g, "$1")
    .replace(/^- /gm, "• ")
    .trim();
}

function escapeHtml(value: string) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function markdownToHtml(markdown: string) {
  const lines = markdown.split("\n");
  return lines.map((line) => {
    if (line.startsWith("### ")) return `<h3>${escapeHtml(line.slice(4))}</h3>`;
    if (line.startsWith("## ")) return `<h2>${escapeHtml(line.slice(3))}</h2>`;
    if (line.startsWith("# ")) return `<h1>${escapeHtml(line.slice(2))}</h1>`;
    if (line.startsWith("- ")) return `<li>${escapeHtml(line.slice(2))}</li>`;
    if (!line.trim()) return `<br />`;
    return `<p>${escapeHtml(line)}</p>`;
  }).join("\n").replace(/(<li>[\s\S]*?<\/li>)/g, "<ul>$1</ul>").replace(/<\/ul>\s*<ul>/g, "");
}

export function buildSummaryMarkdown(summary: LearningProgressSummary, childName: string) {
  const sections = [
    `# ${summary.title}`,
    "",
    `**Child:** ${childName}`,
    `**Period:** ${summary.period_start} to ${summary.period_end}`,
    summary.subject ? `**Subject:** ${summary.subject}` : "",
    `**Evidence count:** ${summary.evidence_count}`,
    `**Confidence:** ${Math.round(Number(summary.confidence) * 100)}%`,
    "",
    "## Executive summary",
    summary.executive_summary,
    "",
    "## Professional narrative",
    summary.narrative_text,
    "",
    "## Strengths",
    ...summary.strengths.map((item) => `- ${item}`),
    "",
    "## Concerns / areas for improvement",
    ...summary.concerns.map((item) => `- ${item}`),
    "",
    "## Observed patterns",
    ...summary.observed_patterns.map((item) => `- ${item}`),
    "",
    "## Recommendations",
    ...summary.recommendations.map((item) => `- ${item}`),
    "",
    "## Parent actions",
    ...summary.parent_actions.map((item) => `- ${item}`),
    "",
    "## Child actions",
    ...summary.child_actions.map((item) => `- ${item}`),
    "",
    "## Teacher / coach questions",
    ...summary.teacher_questions.map((item) => `- ${item}`),
    "",
    "## Next goals",
    ...summary.next_goals.map((item) => `- ${item}`),
    "",
    "## Missing evidence",
    ...(summary.missing_evidence.length ? summary.missing_evidence.map((item) => `- ${item}`) : ["- No major missing evidence was identified."]),
  ].filter(Boolean);

  return sections.join("\n");
}

