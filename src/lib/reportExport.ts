export function openPrintWindow(title: string, markdown: string) {
  const html = buildPrintableHtml({ title, markdown });
  const win = window.open("", "_blank");
  if (!win) throw new Error("Popup blocked. Allow popups to open print view.");
  win.document.write(html);
  win.document.close();
}

export function downloadHtml(filename: string, title: string, markdown: string) {
  const html = buildPrintableHtml({ title, markdown });
  const blob = new Blob([html], { type: "text/html;charset=utf-8" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename.endsWith(".html") ? filename : `${filename}.html`;
  document.body.appendChild(a);
  a.click();
  a.remove();
  URL.revokeObjectURL(url);
}

export function buildEmailDraft(subject: string, body: string) {
  const mailto = `mailto:?subject=${encodeURIComponent(subject)}&body=${encodeURIComponent(body)}`;
  window.location.href = mailto;
}

export async function loadReportShares(familyId: string, summaryId: string) {
  const { data, error } = await supabase
    .from("progress_report_shares")
    .select("*")
    .eq("family_id", familyId)
    .eq("summary_id", summaryId)
    .order("created_at", { ascending: false });

  if (error) throw error;
  return (data ?? []) as ProgressReportShare[];
}

export async function generateReportShareVersion(args: {
  familyId: string;
  summaryId: string;
  audience: ProgressReportShare["audience"];
  language: ProgressReportShare["language"];
  customInstruction?: string | null;
  save?: boolean;
}) {
  const { data, error } = await supabase.functions.invoke("ai-report-share-version", {
    body: {
      family_id: args.familyId,
      summary_id: args.summaryId,
      audience: args.audience,
      language: args.language,
      custom_instruction: args.customInstruction ?? null,
      save: args.save ?? true,
    },
  });

  if (error) throw error;
  return data as { ok: boolean; saved: boolean; share: ProgressReportShare };
}

export async function updateReportShareStatus(args: {
  familyId: string;
  shareId: string;
  status: "draft" | "final" | "archived";
}) {
  const { error } = await supabase
    .from("progress_report_shares")
    .update({ status: args.status })
    .eq("family_id", args.familyId)
    .eq("id", args.shareId);

  if (error) throw error;
}
